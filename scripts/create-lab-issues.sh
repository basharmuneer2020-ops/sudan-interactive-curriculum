# المنهج السوداني التفاعلي
# =============================================================================
#
# المتطلبات:
#   - gh CLI مثبت ومسجل الدخول (gh auth login)
#   - أنت داخل مجلد المشروع (sudan-interactive-curriculum)
#
# الاستخدام:
#   chmod +x scripts/create-lab-issues.sh
#   ./scripts/create-lab-issues.sh
#
# =============================================================================

set -e

REPO="basharmuneer2020-ops/sudan-interactive-curriculum"
echo "🔬 إنشاء Issues للمعامل التفاعلية — الثالث ثانوي..."
echo "📦 المستودع: $REPO"
echo ""

# التحقق من gh CLI
if ! command -v gh &> /dev/null; then
    echo "❌ gh CLI غير مثبت. ثبته من: https://cli.github.com/"
    exit 1
fi

# التحقق من تسجيل الدخول
if ! gh auth status &> /dev/null 2>&1; then
    echo "❌ لم تسجل الدخول. نفذ: gh auth login"
    exit 1
fi

# إنشاء Labels أولاً
echo "🏷️  إنشاء التصنيفات..."
gh label create "lab" --description "معمل تفاعلي جديد" --color "0E8A16" --repo "$REPO" 2>/dev/null || true
gh label create "grade-12" --description "الصف الثالث ثانوي" --color "1D76DB" 2>/dev/null --repo "$REPO" || true
gh label create "physics" --description "مادة الفيزياء" --color "5319E7" --repo "$REPO" 2>/dev/null || true
gh label create "chemistry" --description "مادة الكيمياء" --color "FBCA04" --repo "$REPO" 2>/dev/null || true
gh label create "biology" --description "مادة الأحياء" --color "0075CA" --repo "$REPO" 2>/dev/null || true
gh label create "computer-science" --description "مادة الحاسوب" --color "E4E669" --repo "$REPO" 2>/dev/null || true
gh label create "history" --description "مادة التاريخ" --color "D93F0B" --repo "$REPO" 2>/dev/null || true
gh label create "help wanted" --description "مطلوب مساعدة" --color "008672" --repo "$REPO" 2>/dev/null || true
gh label create "good first issue" --description "مناسب للمبتدئين" --color "7057FF" --repo "$REPO" 2>/dev/null || true
echo "✅ تم إنشاء التصنيفات"
echo ""

# ---- دالة مساعدة ----
create_issue() {
    local title="$1"
    local body="$2"
    local labels="$3"

    echo "📝 إنشاء: $title"
    gh issue create \
        --repo "$REPO" \
        --title "$title" \
        --body "$body" \
        --label "$labels"
    echo "   ✅ تم"
    sleep 1
}

